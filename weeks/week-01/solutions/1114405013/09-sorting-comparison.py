# 9 比較、排序與 key 函式範例
# 本檔案展示如何使用比較運算、排序和 key 函式

print("=" * 60)
print("9. 比較、排序與 key 函式")
print("=" * 60)

# ============================================================
# 1. 比較運算（tuple 逐一比較）
# ============================================================
print("\n1. Tuple 比較（逐一元素比較）")
print("-" * 60)

a = (1, 2)
b = (1, 3)
result = a < b

print(f"a = {a}")
print(f"b = {b}")
print(f"a < b: {result}")
print("# 說明：比較時先比第一個元素，若相同則比第二個")

# ============================================================
# 2. key 排序
# ============================================================
print("\n2. 使用 key 函式排序")
print("-" * 60)

rows = [{'uid': 3}, {'uid': 1}, {'uid': 2}]
rows_sorted = sorted(rows, key=lambda r: r['uid'])

print(f"原始列表: {rows}")
print(f"按 uid 排序後: {rows_sorted}")
print("# 說明：lambda 函式用於指定排序的鍵（key）")

# ============================================================
# 3. min/max 搭配 key
# ============================================================
print("\n3. min/max 搭配 key 函式")
print("-" * 60)

smallest = min(rows, key=lambda r: r['uid'])

print(f"rows 列表: {rows}")
print(f"uid 最小的字典: {smallest}")
print("# 說明：key 函式告訴 min() 要比較哪個欄位")

print("\n" + "=" * 60)
print("範例執行完成")
print("=" * 60)
