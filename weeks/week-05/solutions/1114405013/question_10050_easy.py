import sys


# 題目輸入是很多行整數，我們直接整份讀進來後用 split() 切開，
# 再全部轉成整數，這樣就可以用「指標」從前到後依序讀資料。
nums = list(map(int, sys.stdin.read().split()))

# 若完全沒有輸入，就直接結束程式（避免後面索引錯誤）。
if not nums:
    sys.exit(0)

# 第一個數字是測資組數 T：代表接下來要處理幾組案例。
t = nums[0]

# i 是目前讀到 nums 的位置。
# 因為 nums[0] 已經是 T，所以從 1 開始。
i = 1

# ans 用來收集每組測資的答案，最後再一次輸出。
ans = []

for _ in range(t):
    # 讀 N（模擬天數）：從第 1 天到第 N 天。
    n = nums[i]
    i += 1

    # 讀 P（政黨數）：本組有幾個政黨。
    p = nums[i]
    i += 1

    # 用集合 set 記錄「損失的工作日」。
    # 好處：如果不同政黨在同一天都罷會，set 只會保留一份，
    # 就不會把同一天重複計算多次。
    lost = set()

    # 依序處理每個政黨的罷會參數 h。
    # h 的意思是：每隔 h 天就會發動一次罷會。
    for _ in range(p):
        h = nums[i]
        i += 1

        # 該政黨的罷會日為 h, 2h, 3h, ... 直到超過 N。
        d = h
        while d <= n:
            # 題目規定第 1 天是星期天。
            # d % 7 == 6 -> 星期五（假日）
            # d % 7 == 0 -> 星期六（假日）
            # 週五、週六不算工作日損失，因此要排除。
            if d % 7 != 6 and d % 7 != 0:
                lost.add(d)

            # 前進到這個政黨的下一次罷會日。
            d += h

    # 這組答案就是集合大小（損失了幾個不同的工作日）。
    ans.append(str(len(lost)))

# 依題目格式，每組答案輸出一行。
print("\n".join(ans))
